// Question 3: Binary search trees.

type Cell_BST struct {
	value int
	left  *Cell_BST
	right *Cell_BST
}

type BSTree struct {
	root *Cell_BST
}

func NewBSTree() *BSTree {
	s := &BSTree{nil}
	return s
}

func PrintBSTree(t *BSTree) {
	// Recursive inner function!
	var print func(*Cell_BST, string)
	print = func(c *Cell_BST, prefix string) {
		if c != nil {
			print(c.right, prefix+"    ")
			fmt.Printf("%s%d\n", prefix, c.value)
			print(c.left, prefix+"    ")
		} else {
			fmt.Printf("%s.\n", prefix)
		}
	}
	print(t.root, "  ")
	fmt.Println()
}

func find(node *Cell_BST, k int) (*Cell_BST, bool) {
	if node == nil {
		return nil, false
	}
	if k == node.value {
		return node, true
	} else if k < node.value {
		return find(node.left, k)
	} else {
		return find(node.right, k)
	}
}

func FindBSTree(t *BSTree, k int) (*Cell_BST, bool) {
	return find(t.root, k)
}

func insert(node *Cell_BST, k int) *Cell_BST {
	if node == nil {
		return &Cell_BST{value: k}
	}
	if k < node.value {
		node.left = insert(node.left, k)
	} else if k > node.value {
		node.right = insert(node.right, k)
	}
	return node
}

func InsertBSTree(t *BSTree, k int) {
	t.root = insert(t.root, k)
}

